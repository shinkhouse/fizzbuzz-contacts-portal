export function randomRgba(): string {
    const o = Math.round,
        r = Math.random,
        min = 50,
        max = 220;
    return 'rgb(' + o(r() * (max - min + 1) + min) + ',' + o(r() * (max - min + 1) + min) + ',' + o(r() * (max - min + 1) + min) + ')';
}
